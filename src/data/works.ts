import type { Work } from "../types/zhaojing";

const selectedMd = {
  sourceType: "selected-md" as const,
  sourceName: "参考+藻井.md",
  verificationStatus: "needs-verification" as const,
};

const pdfCatalog = {
  sourceType: "pdf-catalog" as const,
  sourceName: "敦煌历代精品藻井100图.pdf",
  verificationStatus: "needs-verification" as const,
};

type WorkRecord = {
  id: string;
  dynastyId: string;
  periodLabel: string;
  title: string;
  shortTitle: string;
  cave: string;
  imageFile: string;
  tags: string[];
  motifs: string[];
  catalogNos?: number[];
  sourceTitle?: string;
};

const imageBasePath = "/assets/zaojing/originals";

const getLocation = (cave: string) => {
  if (cave.includes("榆林")) {
    return "安西榆林窟";
  }
  if (cave.includes("西千佛洞")) {
    return "敦煌西千佛洞";
  }
  return "敦煌莫高窟";
};

const createWork = (record: WorkRecord): Work => {
  const imagePath = `${imageBasePath}/${record.imageFile}`;
  const catalogNos = record.catalogNos ?? [];

  return {
    id: record.id,
    dynastyId: record.dynastyId,
    title: `${record.cave} · ${record.shortTitle}`,
    shortTitle: record.shortTitle,
    cave: record.cave,
    location: getLocation(record.cave),
    periodLabel: record.periodLabel,
    catalogNos,
    isFeatured: true,
    oneLineSummary: `以${record.motifs.join("、")}呈现${record.periodLabel}藻井的图像秩序。`,
    description: "该作品依据参考资料与本地图片整理，详细结构、色彩与纹样分析待后续补充。",
    images: {
      original: imagePath,
      thumbnail: imagePath,
      alt: `${record.cave}${record.shortTitle}`,
    },
    tags: record.tags,
    motifs: record.motifs,
    palettes: [],
    patterns: [],
    colorHotspots: [],
    patternHotspots: [],
    geneCard: null,
    sourceRefs: [
      {
        ...selectedMd,
        catalogNos,
        originalTitle: record.sourceTitle ?? `${record.periodLabel} · ${record.cave} · ${record.shortTitle}`,
      },
      ...(catalogNos.length > 0
        ? [
            {
              ...pdfCatalog,
              catalogNos,
              originalTitle: `目录第${catalogNos.join("、")}幅`,
            },
          ]
        : []),
    ],
  };
};

